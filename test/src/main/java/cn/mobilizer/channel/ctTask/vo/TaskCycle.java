package cn.mobilizer.channel.ctTask.vo;

public class TaskCycle {
	
	private String dataCycle;
	
	private String dataText;
	
	private Boolean currentCycle;

	public String getDataCycle() {
		return dataCycle;
	}

	public void setDataCycle(String dataCycle) {
		this.dataCycle = dataCycle;
	}

	public String getDataText() {
		return dataText;
	}

	public void setDataText(String dataText) {
		this.dataText = dataText;
	}

	public Boolean getCurrentCycle() {
		return currentCycle;
	}

	public void setCurrentCycle(Boolean currentCycle) {
		this.currentCycle = currentCycle;
	}	 
	
}
